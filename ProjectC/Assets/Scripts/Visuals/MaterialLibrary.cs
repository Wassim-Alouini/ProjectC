using UnityEngine;

public class MaterialLibrary : MonoBehaviour
{
    public Material[] Materials;
    public static MaterialLibrary Instance { get; private set; }
    private void Awake()
    {
        if (Instance == null)
        {
            Instance = this;
            DontDestroyOnLoad(gameObject);
        }
        else
        {
            Destroy(gameObject);
        }
    }

}
